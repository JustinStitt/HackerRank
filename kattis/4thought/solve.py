t = int(input())

no = "no solution"

precompute = {
    256: "4 * 4 * 4 * 4",
    68: "4 + 4 * 4 * 4",
    60: "4 * 4 * 4 - 4",
    16: "4 / 4 * 4 * 4",
    32: "4 * 4 + 4 * 4",
    24: "4 + 4 + 4 * 4",
    17: "4 / 4 + 4 * 4",
    0: "4 / 4 / 4 / 4",
    8: "4 / 4 * 4 + 4",
    15: "4 * 4 - 4 / 4",
    1: "4 / 4 - 4 + 4",
    9: "4 / 4 + 4 + 4",
    -8: "4 - 4 - 4 - 4",
    7: "4 - 4 / 4 + 4",
    4: "4 / 4 / 4 + 4",
    -60: "4 - 4 * 4 * 4",
    -16: "4 - 4 - 4 * 4",
    -1: "4 - 4 / 4 - 4",
    2: "4 / 4 + 4 / 4",
    -15: "4 / 4 - 4 * 4",
    -7: "4 / 4 - 4 - 4",
    -4: "4 / 4 / 4 - 4",
}


def solve(n: int) -> None:
    expression = precompute.get(n, None)
    if expression:
        print(expression, "=", n)
    else:
        print(no)


for _ in range(t):
    n = int(input())
    solve(n)
